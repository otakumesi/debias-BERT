import torch
from torch import nn
from torch.nn import CrossEntropyLoss, MSELoss
from transformers import (
    BertModel,
    BertPreTrainedModel
)
from transformers.modeling_outputs import (
    SequenceClassifierOutput
)

class BertForSequenceClassificationbyWholeLayer(BertPreTrainedModel):
    def __init__(self, config):
        super().__init__(config)
        self.num_labels = config.num_labels

        self.bert = BertModel(config)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)
        self.classifier = nn.Linear(config.hidden_size * config.num_hidden_layers, config.num_labels)

        self.init_weights()

    def forward(
        self,
        input_ids=None,
        attention_mask=None,
        token_type_ids=None,
        position_ids=None,
        head_mask=None,
        inputs_embeds=None,
        labels=None,
    ):
        outputs = self.bert(
            input_ids,
            attention_mask=attention_mask,
            token_type_ids=token_type_ids,
            position_ids=position_ids,
            head_mask=head_mask,
            inputs_embeds=inputs_embeds,
            output_hidden_states=True,
            return_dict=True,
        )

        hidden_states = outputs.hidden_states
        pooled_outputs = torch.stack([self.bert.pooler(hidden_state) for hidden_state in hidden_states[1:]]).permute(1, 0, 2)
        pooled_outputs = self.dropout(pooled_outputs)
        logits = self.classifier(pooled_outputs.reshape(-1, self.config.hidden_size * self.config.num_hidden_layers))

        loss = None
        if labels is not None:
            if self.num_labels == 1:
                #  We are doing regression
                loss_fct = MSELoss()
                loss = loss_fct(logits.view(-1), labels.view(-1))
            else:
                loss_fct = CrossEntropyLoss()
                loss = loss_fct(logits.view(-1, self.num_labels), labels.view(-1))

        return SequenceClassifierOutput(
            loss=loss,
            logits=logits,
            hidden_states=outputs.hidden_states
        )
